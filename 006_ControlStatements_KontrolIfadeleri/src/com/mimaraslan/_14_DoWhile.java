package com.mimaraslan;

public class _14_DoWhile {

/*
 
do{
 
        <Block of statements>;
 
 }while(<boolean condition>);

*/
	public static void main(String[] args) {
		
		int sayi = 1;
        do {
            System.out.println("SONU‚ : " + sayi);
            sayi++;
        } while (sayi < 8);
		
	}

}
