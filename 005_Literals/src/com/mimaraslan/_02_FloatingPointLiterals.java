package com.mimaraslan;

public class _02_FloatingPointLiterals {

	public static void main(String[] args) {
		// bilimsel scientific dip notlar
		double d1 = 123.4;		
		double d2 = 1.234e2;
		float f1  = 123.4f;
		
		System.out.println("normal : " + d1);
		System.out.println("e      : " + d2);
		System.out.println("f      : " + f1);

	}

}
