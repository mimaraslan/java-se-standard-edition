package com.mimaraslan;

public class _01_IntegerLiterals {

	public static void main(String[] args) {
		// Say› 26,  decimal
		int decVal = 26;
		//  Say› 26,  hexadecimal
		int hexVal = 0x1a;
		// Say› 26,  binary
		int binVal = 0b11010;
		
		System.out.println("decimal     : " + decVal);
		System.out.println("hexadecimal : " + hexVal);
		System.out.println("binary      : " + binVal);

	}

}
