package com.mimaraslan;

public class KarakterTipi {
	
	public static void main(String[] args) {
		char karakter1, karakter2;
		
		karakter1 = 65;
		karakter2 = 'F';
		System.out.println("1.ATAMALAR");
		System.out.println("KARAKTER 1 SONUCU : "+ karakter1);
		System.out.println("KARAKTER 2 SONUCU : "+ karakter2);
		
		
		karakter1 = (char) (karakter1 + 1);
		karakter2 = 'K';
		System.out.println("2.ATAMALAR");
		System.out.println("KARAKTER 1 SONUCU : "+ karakter1);
		System.out.println("KARAKTER 2 SONUCU : "+ karakter2);

	}

}
